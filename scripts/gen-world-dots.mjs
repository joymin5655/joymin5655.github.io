// One-off generator: rasterize world landmass (world-atlas land-110m) into a
// compact equirectangular dot grid consumed by the hero canvas.
// Run: node scripts/gen-world-dots.mjs  →  overwrites src/data/worldDots.ts
import { readFileSync, writeFileSync } from 'node:fs';
import { feature } from 'topojson-client';

const topo = JSON.parse(readFileSync('node_modules/world-atlas/land-110m.json', 'utf8'));
const land = feature(topo, topo.objects.land);

// point-in-polygon (ray casting), rings in [lon, lat]
function inRing(pt, ring) {
  let inside = false;
  for (let i = 0, j = ring.length - 1; i < ring.length; j = i++) {
    const [xi, yi] = ring[i];
    const [xj, yj] = ring[j];
    if (yi > pt[1] !== yj > pt[1] && pt[0] < ((xj - xi) * (pt[1] - yi)) / (yj - yi) + xi) inside = !inside;
  }
  return inside;
}
function inLand(lon, lat) {
  for (const f of land.features) {
    const geoms = f.geometry.type === 'Polygon' ? [f.geometry.coordinates] : f.geometry.coordinates;
    for (const poly of geoms) {
      if (inRing([lon, lat], poly[0])) {
        let hole = false;
        for (let h = 1; h < poly.length; h++) if (inRing([lon, lat], poly[h])) { hole = true; break; }
        if (!hole) return true;
      }
    }
  }
  return false;
}

// 2.4° grid, clip Antarctica (< -56°) — dots stored as [x, y] in 0..1 space
const STEP = 2.4;
const dots = [];
for (let lat = 74; lat > -56; lat -= STEP) {
  for (let lon = -168; lon < 180; lon += STEP) {
    if (inLand(lon, lat)) {
      const x = (lon + 180) / 360;
      const y = (90 - lat) / 180;
      dots.push([+x.toFixed(4), +y.toFixed(4)]);
    }
  }
}

const out = `// Generated by scripts/gen-world-dots.mjs — equirectangular landmass dot grid
// (world-atlas land-110m, ${STEP}° step, Antarctica clipped). Do not edit by hand.
export const worldDots: [number, number][] = ${JSON.stringify(dots)};
`;
writeFileSync('src/data/worldDots.ts', out);
console.log(`dots: ${dots.length}, bytes: ${out.length}`);
